        <p>Seja você um desenvolvedor experiente, um academico, um estudante área relacionada a construção de sites para web ou iniciantes, <strong>Construindo Sites com CSS e (X)HTML</strong> ampliara seus conhecimentos e sera uma fonte de consulta valiosa no seu dia-a-dia no trabalho.<br>
        Este livro aborda a técnica de criação de sites baseada na Web Standards Visual do W3C, cujo príncipio básico é separar (X)HTML da apresentação visual. As CSSs são uma poderosa ferramenta a disposição do desenvolvedor para criar um arquivo central do controle do layout e apresentação do site.<br>
        De forma didática, são apresentada as razões de criação de sites Standards e sua vantagens, a sintaxe (X)HTML  e CSS, a propriedades CSS e a criação de layouts fixos e elásticos multicolunares. Os assuntos tratados são ilustrados com códigos, exemplos e figuras que mostrão a renderização do navegador. No site de apoio, o leitor encontrara os códigos para apoio e informaçoes complementares sobre o livro.</p>
